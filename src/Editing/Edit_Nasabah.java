    public Object [][] getData(){
    Object [][] data=null;
    try{
        Class.forName("com.mysql.jdbc.Driver");
        conn=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/pegadaian?;","root","");
        st= (Statement) conn.createStatement();
        String Query="Select * from nasabah;";
        rs=st.executeQuery(Query);
        rs.last();
        int baris=rs.getRow();
        rs.beforeFirst();        
        data=new Object[baris][4];
        int no=-1;
        while(rs.next()){
            no++;
            data[no][0]=rs.getString("idnasabah");
            data[no][1]=rs.getString("nama");
            data[no][2]=rs.getString("alamat");
            data[no][3]=rs.getString("no_telp");
        }
        st.close();
    }
    catch(Exception e){
        System.out.print("Error Karena = "+e);
    }
    return data;
}
