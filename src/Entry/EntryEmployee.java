    private void btdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdeleteActionPerformed
        // TODO add your handling code here:
        String idemployee=tbEmployee.getValueAt(tbEmployee.getSelectedRow(),0).toString();
        JOptionPane.showMessageDialog(rootPane, "idemployee ="+idemployee);
        int konf=JOptionPane.showConfirmDialog(this,"Apakah anda yakin ingin dihapus ?","delete",JOptionPane.YES_NO_CANCEL_OPTION);

        if(konf==JOptionPane.YES_OPTION){
            try{
                konek.konek();
                PreparedStatement ps=null;
                String query="DELETE FROM employee WHERE idemployee=?";
                ps=(PreparedStatement) conn.prepareStatement(query);
                ps.setString(1, idemployee);
                int ok=ps.executeUpdate();
                if(ok==1)
                    JOptionPane.showMessageDialog(this,"DELETE berhasil","DELETE",JOptionPane.INFORMATION_MESSAGE);
                else
                    JOptionPane.showMessageDialog(this,"DELETE GAGAL","DELETE",JOptionPane.INFORMATION_MESSAGE);
                ps.close();
                showTable();
            } catch (Exception e){

            }
        } else{
            JOptionPane.showMessageDialog(this,"DELETE CANCELED","DELETE",JOptionPane.INFORMATION_MESSAGE);
        }
}//GEN-LAST:event_btdeleteActionPerformed

