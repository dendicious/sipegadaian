    private void BTUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTUPDATEActionPerformed
        // TODO add your handling code here:
try{
        konek.konek();
        PreparedStatement ps=null;
        String query="update nasabah set nama=?, alamat=? , no_telp=? where idnasabah=?";
        ps= (PreparedStatement) conn.prepareStatement(query);
        ps.setString(1, txtnama.getText());
        ps.setString(2, txtalamat.getText());
        ps.setString(3, txttelp.getText());
        ps.setString(4, txtidnasabah.getText());

        int ok=ps.executeUpdate();
        if(ok>0){
            JOptionPane.showMessageDialog(this, "Edit data Nasabah sukses","Informasi",JOptionPane.INFORMATION_MESSAGE);
            showTable(getData());
        }
        else{
            JOptionPane.showMessageDialog(this, "Edit data Nasabah gagal","Informasi",JOptionPane.INFORMATION_MESSAGE);
            showTable(getData());
        }
        ps.close();
        isClear();
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null,"Editing data gagal karena "+e);
    }
}//GEN-LAST:event_BTUPDATEActionPerformed
