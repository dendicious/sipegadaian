public Object [][] getData(){
    Object [][]data=null;
    try{
        Class.forName("com.mysql.jdbc.Driver");
        conn=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/pegadaian?;","root","");
        st=(Statement) conn.createStatement();
        String query="SELECT * FROM employee;";
        rs=st.executeQuery(query);
        rs.last();
        int baris=rs.getRow();
        rs.beforeFirst();
        data=new Object[baris][3];
        int no=-1;
        while(rs.next()){
            no++;
            data[no][0]=rs.getString("idemployee");
            data[no][1]=rs.getString("nama");
            data[no][2]=rs.getString("alamat");
        }
        st.close();
    }
    catch(Exception e){

    }
    return data;
}
public void showTable(){
    String []nama_kolom={"idemployee","nama","alamat"};
    tbEmployee =new JTable(getData(), nama_kolom);
    jScrollPane1.setViewportView(tbEmployee);
}

